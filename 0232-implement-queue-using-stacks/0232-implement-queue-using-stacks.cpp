class MyQueue {
    stack<int> s1;
    stack<int> s2;

    void move(stack<int> &from, stack<int> &to) {
        while (!from.empty()) {
            to.push(from.top());
            from.pop();
        }
    }

public:
    MyQueue() {

    }

    void push(int x) {
        move(s1, s2);
        s1.push(x);
        move(s2, s1);
    }

    int pop() {
        int val = s1.top();
        s1.pop();
        return val;
    }

    int peek() {
        return s1.top();
    }

    bool empty() {
        return s1.empty();
    }
};

